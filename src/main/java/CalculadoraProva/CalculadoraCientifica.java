package CalculadoraProva;
import java.lang.Math;

/**
 *
 * @author Sabrina Fernandes de Sousa
 */

public class CalculadoraCientifica {
    
    public double raizQuadrada(double a){
        return Math.sqrt(a);
    }
    
    public double potencia(double a, double b){
        return Math.pow(a, b);
    }
}
